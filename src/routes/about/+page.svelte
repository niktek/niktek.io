			<h2 class="h2">What Does NASA Do?</h2>
			<p>
				Many people know something about NASA’s work. But most probably have no idea about how many
				different things the agency does. Astronauts in orbit conduct scientific research.
				Satellites help scientists learn more about Earth. Space probes study the solar system and
				beyond. New developments improve air travel and other aspects of flight. NASA is also
				beginning a new program to send humans to explore the Moon and Mars. In addition to those
				major missions, NASA does many other things. The agency shares what it learns so that its
				information can make life better for people worldwide. For example, companies can use NASA
				discoveries to create new spinoff products.
			</p>
			<p>
				NASA helps teachers prepare students who will be the engineers, scientists, astronauts and
				other NASA workers of the future. They will be the adventurers who will continue exploration
				of the solar system and universe. NASA has a tradition of investing in programs and
				activities that inspire students, educators, families and communities in the excitement and
				discovery of exploration. NASA offers training to help teachers learn new ways to teach
				science, technology, engineering and mathematics. The agency also involves students in NASA
				missions to help them get excited about learning.
			</p>

			<h2 class="h2">Who Works for NASA?</h2>
			<p>
				NASA’s Headquarters is in Washington, D.C. The agency has nine centers, the Jet Propulsion
				Laboratory and seven test and research facilities located in several states around the
				country. More than 17,000 people work for NASA. Many more people work with the agency as
				government contractors. These people are hired by companies that NASA pays to do work. The
				combined workforce represents a variety of jobs. Astronauts may be the best-known NASA
				employees, but they only represent a small number of the total workforce. Many NASA workers
				are scientists and engineers. But people there hold many other jobs, too, from secretaries
				to writers to lawyers to teachers.
			</p>

			<h2 class="h2">What Has NASA Done?</h2>
			<p>
				When NASA started, it began a program of human spaceflight. The Mercury, Gemini and Apollo
				programs helped NASA learn about flying in space and resulted in the first human landing on
				the Moon in 1969. Currently, NASA has astronauts living and working on the International
				Space Station. NASA’s robotic space probes have visited every planet in the solar system and
				several other celestial bodies. Telescopes have allowed scientists to look at the far
				reaches of space. Satellites have revealed a wealth of data about Earth, resulting in
				valuable information such as a better understanding of weather patterns. NASA has helped
				develop and test a variety of cutting-edge aircraft. These aircraft include planes that have
				set new records. Among other benefits, these tests have helped engineers improve air
				transportation. NASA technology has contributed to many items used in everyday life, from
				smoke detectors to medical tests.
